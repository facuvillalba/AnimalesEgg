
public class Patolucas {
    
    private String nom;
    private String color;
    private String edad;
    
    
    
    public Patolucas(){
     
    }

    public Patolucas(String nom, String color, String edad) {
        this.nom = nom;
        this.color = color;
        this.edad = edad;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }
    
    
    
    
}
